
import React, { useState, useEffect } from 'react';
import { NavLink } from 'react-router-dom';
import { 
  Home, 
  Calendar, 
  Trophy, 
  Users, 
  ShieldCheck, 
  CreditCard, 
  LogOut, 
  Menu, 
  X, 
  Sparkles,
  User,
  LayoutDashboard
} from 'lucide-react';
import { useApp } from '../App';
import AssistantModal from './AssistantModal';

const Layout: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const { db, user, logout } = useApp();
  const [isAssistantOpen, setIsAssistantOpen] = useState(false);
  const [isSidebarOpen, setIsSidebarOpen] = useState(false);

  const appBranding = db.settings?.appBranding || {
    appName: 'FNPE - Federação Norte de Pesca Esportiva',
    appLogoDataUrl: ''
  };

  useEffect(() => {
    document.title = appBranding.appName;
  }, [appBranding.appName]);

  const menuItems = [
    { icon: Home, label: 'Início', path: '/app' },
    { icon: CreditCard, label: 'ID Norte', path: '/app/id-norte' },
    { icon: Calendar, label: 'Eventos', path: '/app/eventos' },
    { icon: Trophy, label: 'Rankings', path: '/app/ranking-estadual' },
    { icon: Users, label: 'Atletas', path: '/app/atletas' },
    { icon: User, label: 'Perfil', path: '/app/perfil' },
  ];

  if (user?.nivel === 'ADMIN') {
    menuItems.splice(1, 0, { icon: LayoutDashboard, label: 'Dashboard', path: '/app/admin-dashboard' });
    menuItems.push({ icon: ShieldCheck, label: 'Admin', path: '/app/admin' });
  }

  return (
    <div className="min-h-screen bg-slate-950 text-slate-100 flex flex-col md:flex-row">
      {/* Sidebar - Fixa e Escura */}
      <aside className="hidden md:flex flex-col w-64 bg-slate-900 border-r border-slate-800 sticky top-0 h-screen z-40">
        <div className="p-8 border-b border-slate-800 flex items-center gap-3">
          {appBranding.appLogoDataUrl ? (
            <img src={appBranding.appLogoDataUrl} alt="Logo" className="w-8 h-8 object-contain" />
          ) : (
            <Trophy className="text-emerald-500 w-8 h-8" />
          )}
          <span className="text-lg font-black tracking-tighter text-white">FNPE</span>
        </div>
        
        <nav className="flex-1 p-4 space-y-1 overflow-y-auto">
          {menuItems.map((item) => (
            <NavLink
              key={item.path}
              to={item.path}
              end={item.path === '/app'}
              className={({ isActive }) => 
                `flex items-center gap-3 px-4 py-3 rounded-xl transition-all ${
                  isActive 
                    ? 'bg-emerald-500/10 text-emerald-400 font-bold' 
                    : 'text-slate-400 hover:text-slate-100 hover:bg-slate-800/50'
                }`
              }
            >
              <item.icon className="w-5 h-5" />
              <span className="text-sm">{item.label}</span>
            </NavLink>
          ))}
        </nav>

        <div className="p-4 border-t border-slate-800 space-y-1">
          <button
            onClick={() => setIsAssistantOpen(true)}
            className="flex w-full items-center gap-3 px-4 py-3 rounded-xl text-emerald-400 hover:bg-emerald-500/10 transition-all text-sm font-bold"
          >
            <Sparkles className="w-5 h-5" />
            Assistente IA
          </button>
          <button
            onClick={logout}
            className="flex w-full items-center gap-3 px-4 py-3 rounded-xl text-slate-500 hover:text-red-400 hover:bg-red-400/5 transition-all text-sm"
          >
            <LogOut className="w-5 h-5" />
            Sair
          </button>
        </div>
      </aside>

      {/* Mobile Top Header */}
      <header className="md:hidden bg-slate-900 border-b border-slate-800 p-4 sticky top-0 z-30 flex items-center justify-between">
        <div className="flex items-center gap-2">
          <Trophy className="text-emerald-500 w-6 h-6" />
          <span className="font-black text-white uppercase tracking-widest text-sm">FNPE</span>
        </div>
        <div className="flex gap-2">
          <button onClick={() => setIsAssistantOpen(true)} className="p-2 text-emerald-400"><Sparkles className="w-6 h-6" /></button>
          <button onClick={() => setIsSidebarOpen(!isSidebarOpen)} className="p-2 text-slate-400">
            {isSidebarOpen ? <X className="w-6 h-6" /> : <Menu className="w-6 h-6" />}
          </button>
        </div>
      </header>

      {/* Layout Mobile Overlay Menu */}
      {isSidebarOpen && (
        <div className="md:hidden fixed inset-0 z-40 flex">
          <div className="bg-slate-900 w-64 h-full border-r border-slate-800 flex flex-col shadow-2xl animate-in slide-in-from-left duration-300">
             <div className="p-8 border-b border-slate-800 flex items-center justify-between">
                <span className="text-lg font-black text-white">MENU</span>
                <button onClick={() => setIsSidebarOpen(false)}><X className="text-slate-400" /></button>
             </div>
             <nav className="flex-1 p-4 space-y-2">
               {menuItems.map(item => (
                 <NavLink 
                    key={item.path} 
                    to={item.path} 
                    onClick={() => setIsSidebarOpen(false)}
                    className={({isActive}) => `flex items-center gap-3 p-4 rounded-xl font-bold ${isActive ? 'bg-emerald-500/10 text-emerald-500' : 'text-slate-400'}`}
                 >
                   <item.icon className="w-5 h-5" /> {item.label}
                 </NavLink>
               ))}
             </nav>
             <div className="p-4 border-t border-slate-800">
                <button onClick={logout} className="flex items-center gap-3 p-4 text-red-400 w-full font-bold"><LogOut className="w-5 h-5" /> Sair</button>
             </div>
          </div>
          <div className="flex-1 bg-black/60 backdrop-blur-sm" onClick={() => setIsSidebarOpen(false)}></div>
        </div>
      )}

      {/* Main Content Area - CENTRALIZADO e COMPACTO (max-w-4xl) */}
      <main className="flex-1 overflow-auto bg-slate-950">
        <div className="max-w-4xl mx-auto w-full p-4 md:p-10 pb-24 md:pb-10 min-h-screen">
          {children}
        </div>
      </main>

      {/* Mobile Bottom Nav - Only for basic items */}
      <nav className="md:hidden fixed bottom-0 left-0 right-0 bg-slate-900/90 backdrop-blur-md border-t border-slate-800 flex justify-around p-3 z-30">
        {menuItems.slice(0, 4).map((item) => (
          <NavLink
            key={item.path}
            to={item.path}
            className={({ isActive }) => 
              `flex flex-col items-center gap-1 transition-all ${
                isActive ? 'text-emerald-500' : 'text-slate-500'
              }`
            }
          >
            <item.icon className="w-5 h-5" />
            <span className="text-[10px] font-black uppercase tracking-tighter">{item.label}</span>
          </NavLink>
        ))}
      </nav>

      {isAssistantOpen && <AssistantModal onClose={() => setIsAssistantOpen(false)} />}
    </div>
  );
};

export default Layout;
